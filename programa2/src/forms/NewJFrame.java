    private void minRangeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_minRangeKeyTyped
        System.out.println(minRange.getText());
        char enter = evt.getKeyChar();
        verificarTxtVacio(evt);
        if ((!(Character.isDigit(enter))) || (minRange.getText().length() > MAX_MIN_RANGE_TXT)) {
            evt.consume();
        }
        System.out.println(minRange.getText());
    }//GEN-LAST:event_minRangeKeyTyped

    private void verificarTxtVacio(java.awt.event.KeyEvent evt) {
        int minRangeInt;
        int maxRangeInt;
        if ((minRange.getText().length() != 0) && (maxRange.getText().length() != 0)) {
            minRangeInt = Integer.parseInt(minRange.getText());
            maxRangeInt = Integer.parseInt(maxRange.getText());
        } else {
            minRangeInt = 0;
            maxRangeInt = 0;
        }
        if (minRangeInt > maxRangeInt) {
            evt.consume();
            maxRange.setBackground(Color.RED);
        } else {
            maxRange.setBackground(Color.WHITE);
        }
    }

    private void maxRangeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_maxRangeKeyTyped
        char enter = evt.getKeyChar();
        if (!(Character.isDigit(enter))) {
            evt.consume();
        } else if (maxRange.getText().length() > MAX_MIN_RANGE_TXT) {
            evt.consume();
        }
    }//GEN-LAST:event_maxRangeKeyTyped

